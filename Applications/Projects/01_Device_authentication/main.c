#define STSAFE_CERTIFICATE_ZONE_0 0U
#define STSE_STATIC_PRIVATE_KEY_SLOT_0 0U

int main(void) {
    stse_ReturnCode_t stse_ret = STSE_API_INVALID_PARAMETER;
    stse_Handler_t stse_handler;
    static const uint8_t ca_selfsigned_cert[] = {CA_SELF_SIGNED_CERTIFICATE_01};
#ifdef STSE_CONF_USE_STATIC_PERSONALIZATION_INFORMATIONS
    static stse_perso_info_t stsafe_a120_spl05_perso_info = {.cmd_AC_status = 0x5555555555545555, .ext_cmd_AC_status = 0x5555555555555555};
#endif

    /* - Initialize Terminal */
    apps_terminal_init(115200);

    /* - Print Example instruction on terminal */
    printf("----------------------------------------------------------------------------------------------------------------");
    printf("\n\r-                                STSAFE-A120 Device Authentication Example                                     -");
    printf("\n\r----------------------------------------------------------------------------------------------------------------");
    printf("\n\r- This example illustrates STSAFE-A120 device authentication process using Multi-Step approach.                -");
    printf("\n\r- it can be taken as reference for building distant server authentication use cases.                           -");
    printf("\n\r----------------------------------------------------------------------------------------------------------------");

    /* ## Initialize STSAFE-A1xx device handler */
    stse_ret = stse_set_default_handler_value(&stse_handler);
    if (stse_ret != STSE_OK) {
        printf(PRINT_RED "\n\r ## stse_set_default_handler_value ERROR : 0x%04X\n\r", stse_ret);
        apps_process_error(stse_ret);
    }

    stse_handler.device_type = STSAFE_A120;
    stse_handler.io.busID = 1;
    stse_handler.io.BusSpeed = 400;
#ifdef STSE_CONF_USE_STATIC_PERSONALIZATION_INFORMATIONS
    stse_handler.perso_info = stsafe_a120_spl05_perso_info; // Use STSAFE-A120 SPL05 static personalization information
#endif

    printf("\n\r - Initialize target STSAFE-A120");
    stse_ret = stse_init(&stse_handler);
    if (stse_ret != STSE_OK) {
        printf(PRINT_RED "\n\r ## stse_init ERROR : 0x%04X\n\r", stse_ret);
        apps_process_error(stse_ret);
    }

    /* ## Authenticate target STSAFE-A120 device */
    stse_ret = stse_device_authenticate(
        &stse_handler,
        ca_selfsigned_cert,
        STSAFE_CERTIFICATE_ZONE_0,
        STSE_STATIC_PRIVATE_KEY_SLOT_0);

    if (stse_ret != STSE_OK) {
        printf(PRINT_RED "\n\n\r ## Device authentication over ST SPL05 CA certificate  : Failed \n\r");
        printf("\n\r\t o stse_device_authenticate ERROR : 0x%04X", stse_ret);
        apps_process_error(stse_ret);
    } else {
        printf(PRINT_GREEN "\n\n\r ## Device authentication over ST SPL05 CA certificate : Successful\n\r");
    }

    while (1) {
        // infinite loop
    }

    return 0;
}
